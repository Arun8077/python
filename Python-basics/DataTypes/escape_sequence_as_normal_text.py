# Output: Line A \n Line B

print("Line A \\n Line B")

# Output: Line A \t Line B

print("Line A \\t Line B")

# Output: This is double backslash \\

print("This is double backslash \\\\")

# Output: This is 4 backslash \\\\

print("This is 4 backslash \\\\\\\\")

# Output \' \""
print(" \\\' \\\"")

